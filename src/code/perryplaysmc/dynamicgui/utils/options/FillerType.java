@SuppressWarnings("unused")
public enum FillerType {
    /**
     * Covers top row
     */
    TOP,
    /**
     * Covers middle row
     */
    MIDDLE,
    /**
     * Covers bottom row
     */
    BOTTOM,
    /**
     * Covers walls/ceiling top/bottom/left/right sides
     */
    BORDER,
    /**
     * Covers all empty spaces
     */
    ALL,
    /**
     * Covers the first row
     */
    TOP_ROW,
    /**
     * Covers the bottom row
     */
    BOTTOM_ROW,
    /**
     * Covers the first row
     */
    ROW_1,
    /**
     * Covers the second row or bottom if 2nd isn't available
     */
    ROW_2,
    /**
     * Covers the third row or bottom if 3rd isn't available
     */
    ROW_3,
    /**
     * Covers the fourth row or bottom if 4th isn't available
     */
    ROW_4,
    /**
     * Covers the fifth row or bottom if 5th isn't available
     */
    ROW_5,
    /**
     * Covers the sixth row or bottom if 6th isn't available
     */
    ROW_6

}
